# começando parte prática da aula 08
from math import sqrt, floor
num = int(input('Digite um número: '))
raiz = sqrt(num)
print(f'A raiz quadrada de {num} é igual a {raiz:.2f}.') # formatação para casas decimais {variável:.2f}
# outra forma: {num}, {floor(raiz)}
